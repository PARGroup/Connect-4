package com.pargroup.model;

import javafx.geometry.Insets;

/**
 * @author Rawad Aboudlal
 *
 */
public class BoardConfig {

  private int columns;
  private int rows;

  private int boardWidth;
  private int boardHeight;

  private int chipWidth;
  private int chipHeight;

  private int horizontalGap;
  private int verticalGap;

  private Insets boardInsets;

  /**
   * @return the columns
   */
  public int getColumns() {
    return columns;
  }

  /**
   * @param columns the columns to set
   */
  public void setColumns(int columns) {
    this.columns = columns;
  }

  /**
   * @return the rows
   */
  public int getRows() {
    return rows;
  }

  /**
   * @param rows the rows to set
   */
  public void setRows(int rows) {
    this.rows = rows;
  }

  /**
   * @return the boardWidth
   */
  public int getBoardWidth() {
    return boardWidth;
  }

  /**
   * @param boardWidth the boardWidth to set
   */
  public void setBoardWidth(int boardWidth) {
    this.boardWidth = boardWidth;
  }

  /**
   * @return the boardHeight
   */
  public int getBoardHeight() {
    return boardHeight;
  }

  /**
   * @param boardHeight the boardHeight to set
   */
  public void setBoardHeight(int boardHeight) {
    this.boardHeight = boardHeight;
  }

  /**
   * @return the chipWidth
   */
  public int getChipWidth() {
    return chipWidth;
  }

  /**
   * @param chipWidth the chipWidth to set
   */
  public void setChipWidth(int chipWidth) {
    this.chipWidth = chipWidth;
  }

  /**
   * @return the chipHeight
   */
  public int getChipHeight() {
    return chipHeight;
  }

  /**
   * @param chipHeight the chipHeight to set
   */
  public void setChipHeight(int chipHeight) {
    this.chipHeight = chipHeight;
  }

  /**
   * @return the horizontalGap
   */
  public int getHorizontalGap() {
    return horizontalGap;
  }

  /**
   * @param horizontalGap the horizontalGap to set
   */
  public void setHorizontalGap(int horizontalGap) {
    this.horizontalGap = horizontalGap;
  }

  /**
   * @return the verticalGap
   */
  public int getVerticalGap() {
    return verticalGap;
  }

  /**
   * @param verticalGap the verticalGap to set
   */
  public void setVerticalGap(int verticalGap) {
    this.verticalGap = verticalGap;
  }

  /**
   * @return the boardInsets
   */
  public Insets getBoardInsets() {
    return boardInsets;
  }

  /**
   * @param boardInsets the boardInsets to set
   */
  public void setBoardInsets(Insets boardInsets) {
    this.boardInsets = boardInsets;
  }

}
